        <h2>About Us</h2>
        <img src="/cssFiles/rowanUniversityrobinson.jpg" alt="">
        <p>The US National Vulnerability Database (NVD) added 12,174 Common Vulnerability and Exposure (CVE) entries in 2019.Each CVE entry acts as a universal identifier for publicly disclosed cybersecurity vulnerabilities and applicability to specific software and versions. The US Department of Homeland Security (DHS) maintains the National Cyber Awareness System, publishing important update bulletins and alerts for critical advanced persistent threat (APT) activity and other threat information.  This deluge of alerts can overwhelm any security engineer.  Regardless, the key to system cybersecurity, maintaining situational awareness, and continuous monitoring of a systems security posture requires constant observation of these security vulnerabilities, threats, and news feeds.</p>
        <p>Our web application helps with this overwhelming issue with the deluge of alerts. Our website is called V.A.S.T, which stands for Vulnerability Alert System Tool.  V.A.S.T automates monitoring online sources of vulnerability information and alert feeds. The software filters the observed data to only relevant notifications based on applicable software, threat actors, or attack targets.   
        A user can create an account, choose their filters and how frequently they want to be alerted. Any data deemed appropriate to a triggers the software to alert the security engineers, forwarding on the relevant data for their evaluation.</p>
